def sumar(op1, op2):
    """Devuelve la suma de dos números"""
    print(f"El resultado de la suma es : {op1 + op2}")


def restar(op1, op2):
    """Devuelve la resta de dos números"""
    print(f"El resultado de la resta es : {op1 - op2}")



