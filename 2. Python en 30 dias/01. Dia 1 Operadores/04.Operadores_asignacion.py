# -------------------------------
# Operador de suma
# Asigna el resultado de la suma (10 + 20) a la variable 'a'
# -------------------------------
a = 10 + 20
print("Resultado de la suma:", a)

# -------------------------------
# Operador de resta
# Asigna el resultado de la resta (10 - 20) a la variable 'b'
# -------------------------------
b = 10 - 20
print("Resultado de la resta:", b)

# -------------------------------
# Operador módulo (%)
# Devuelve el residuo de la división (10 % 5)
# -------------------------------
c = 10 % 5
print("Resultado del módulo:", c)

# -------------------------------
# Operador de división (/)
# Asigna el resultado de dividir (10 / 2) a la variable 'd'
# -------------------------------
d = 10 / 2
print("Resultado de la división:", d)

# -------------------------------
# Operador de potencia (**)
# Calcula 10 elevado a la 2
# -------------------------------
e = 10 ** 2
print("Resultado de la potencia:", e)
