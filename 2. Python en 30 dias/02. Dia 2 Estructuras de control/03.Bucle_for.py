# -----------------------------------------
# Lista de lenguajes de programación
# -----------------------------------------
lenguajes = ["Python", "Java", "Ruby", "C++", "Cobol"]

# -----------------------------------------
# Primer bucle for
# Recorre los elementos de la lista 'lenguajes'
# e imprime cada uno de ellos.
# -----------------------------------------
for i in lenguajes:  
    print(i)

# -----------------------------------------
# Segundo bucle for
# Utiliza range(1, 11) para generar una secuencia
# de números del 1 al 10 e imprimirlos en pantalla.
# -----------------------------------------
for j in range(1, 11):  
    print(j)
