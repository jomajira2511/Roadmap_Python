#Declaracion de variables

Num1 = 1000
Num2 = 500

#Operadores de comparaciones 

print(Num1 > Num2) #operador mayor que 
print(Num1 < Num2) #operador menor que
print(Num1 >= Num2) #operador mayor o igual que 
print(Num1 <= Num2) #operador menor o igual que
print(Num1 == Num2) #operador comparacion 
print(Num1 != Num2) #operador diferente que 

