# Declaración de una lista con elementos de tipo string
Lista = ["Python", "Java", "Ruby"]  # Se pueden incluir datos de cualquier tipo

# Imprimir toda la lista completa
print(Lista)

# Acceder al primer elemento de la lista (índice 0)
print(Lista[0])

# Modificar el valor del primer elemento de la lista
Lista[0] = "SQL"

# Imprimir la lista modificada
print(Lista)
