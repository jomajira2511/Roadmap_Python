Suma = 2 + 2  ##operador de suma es +
print("\n La suma de 2 + 2 es ", Suma)

Resta = 2 - 2  ##operador de resta es -
print("\n La resta de 2 -2 es ", Resta)

Multiplicacion = 2 * 2  ##operador de multiplicacion es *
print("\n La multiplicacion de 2 * 2 es ", Multiplicacion)

Division = 2 / 2  ##operador de division es /
print("\n La division de 2 /2 es ", Division)

Exponenciacion = 2 ** 2  ##operador de exponenciacion es **
print("\n El exponente de 2 ** 2 es ", Exponenciacion)

DivisionEntera= 2 // 2  ##operador de division entera  es //
print("\n La division entera de 2 // 2 es ", DivisionEntera)

Modulo = 2 % 2  ##operador de modulo es %
print("\n El modulo de 2 % 2 es ", Modulo)